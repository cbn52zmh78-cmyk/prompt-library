def pick_instruction(source: str, latin: str) -> str:
    author = source.split(",")[0].split(" (")[0]
    if author in ORATORY_SOURCES and random.random() < 0.4:
        return random.choice(INSTRUCTIONS_ORATORY)
    if author in PHILOSOPHY_SOURCES and random.random() < 0.35:
        return random.choice(INSTRUCTIONS_PHILOSOPHY)
    if author in POETRY_SOURCES or any(w in source for w in ("Aeneid", "Odes", "Eclogues", "Georgics", "Metamorphoses", "Carmen", "Amores")):
        if random.random() < 0.5:
            return random.choice(INSTRUCTIONS_POETRY)
    return random.choice(INSTRUCTIONS)

def main():
    passages = list(PASSAGES)
    assert len(passages) == 150, f"Expected 150 passages, got {len(passages)}"

    # Deduplicate Latin inputs
    seen = set()
    for i, (latin, english, note, source) in enumerate(passages):
        if latin in seen:
            raise ValueError(f"Duplicate Latin at index {i}: {latin[:60]}...")
        seen.add(latin)

    pairs = []
    for latin, english, note, source in passages:
        instr = pick_instruction(source, latin)
        output = (
            f"**Translation:** {english}\n\n"
            f"**Translator's note:** {note}\n\n"
            f"**Source:** {source} [attested]"
        )
        pairs.append({"instruction": instr, "input": latin, "output": output})

    out_path = Path(__file__).parent / "classical_latin_translation_150.jsonl"
    with out_path.open("w", encoding="utf-8") as f:
        for p in pairs:
            f.write(json.dumps(p, ensure_ascii=False) + "\n")

    print(f"Wrote {len(pairs)} pairs to {out_path}")

if __name__ == "__main__":
    main()